public class Name {
    public static void main(String[] args) {
        String[] birds = {"Sparrow", "Parrot", "Peacock", "Eagle", "Flamingo", "Owl", "Penguin", "Kingfisher"};

        System.out.println("List of Bird Names:");
        for (String bird : birds) {
            System.out.println("- " + bird);
        }
    }
}
